// Control Structures
weekday = "monday"
switch (weekday) {
    case "monday":
    case "tuesday":
    case "wednesday":
    case "thursday":
    case "friday":
        console.log("work hard!");
        break;
    case "saturday":
    case "sunday":
        console.log("rest!")
        break;
    default:
        console.log("no such week day!")
        break;
}

